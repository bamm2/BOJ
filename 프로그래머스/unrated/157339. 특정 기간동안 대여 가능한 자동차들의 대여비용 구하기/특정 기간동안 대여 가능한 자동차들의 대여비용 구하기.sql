SELECT CC.CAR_ID,CC.CAR_TYPE, ROUND((DAILY_FEE*(1-DISCOUNT_RATE/100))*30) as FEE
FROM CAR_RENTAL_COMPANY_CAR as CC JOIN
    CAR_RENTAL_COMPANY_DISCOUNT_PLAN as DP
    USING(CAR_TYPE)
WHERE CAR_TYPE IN ('세단','SUV')
    AND DURATION_TYPE = "30일 이상"
    AND CC.CAR_ID NOT IN ( SELECT CAR_ID
                         FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
                         WHERE (START_DATE BETWEEN "2022-11-01" AND "2022-11-30")
                         OR (END_DATE BETWEEN "2022-11-01" AND "2022-11-30")
                         OR (START_DATE <"2022-11-01" AND END_DATE>"2022-11-30"))
   AND ROUND((DAILY_FEE*(1-DISCOUNT_RATE/100))*30) >= 500000 AND ROUND((DAILY_FEE*(1-DISCOUNT_RATE/100))*30) <2000000 
ORDER BY 3 DESC , 2 ASC , 1 DESC ;